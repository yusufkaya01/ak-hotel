// Language management
let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;
    
    // Hero Section
    const heroTitle = document.getElementById('hero-title');
    const heroSubtitle = document.getElementById('hero-subtitle');
    const bookNowBtn = document.getElementById('book-now-btn');
    const exploreBtn = document.getElementById('explore-btn');
    
    if (heroTitle) heroTitle.textContent = content[lang].heroTitle;
    if (heroSubtitle) heroSubtitle.textContent = content[lang].heroSubtitle;
    if (bookNowBtn) bookNowBtn.textContent = content[lang].bookNowBtn;
    if (exploreBtn) exploreBtn.textContent = content[lang].exploreBtn;

    // Welcome Section
    const welcomeTitle = document.getElementById('welcome-title');
    const welcomeText = document.getElementById('welcome-text');
    
    if (welcomeTitle) welcomeTitle.textContent = content[lang].welcomeTitle;
    if (welcomeText) welcomeText.textContent = content[lang].welcomeText;

    // Amenities Section
    const amenitiesTitle = document.getElementById('amenities-title');
    const amenitiesSubtitle = document.getElementById('amenities-subtitle');
    
    if (amenitiesTitle) amenitiesTitle.textContent = content[lang].amenitiesTitle;
    if (amenitiesSubtitle) amenitiesSubtitle.textContent = content[lang].amenitiesSubtitle;
    
    // Individual amenities
    const amenityElements = [
        'amenity-pool', 'amenity-pool-desc',
        'amenity-breakfast', 'amenity-breakfast-desc',
        'amenity-minibar', 'amenity-minibar-desc',
        'amenity-safe', 'amenity-safe-desc',
        'amenity-tv', 'amenity-tv-desc',
        'amenity-wifi', 'amenity-wifi-desc',
        'amenity-ac', 'amenity-ac-desc',
        'amenity-bar', 'amenity-bar-desc'
    ];
    
    amenityElements.forEach(id => {
        const element = document.getElementById(id);
        // Convert kebab-case to camelCase properly
        const key = id.replace(/-([a-z])/g, (match, letter) => letter.toUpperCase());
        if (element && content[lang][key]) {
            element.textContent = content[lang][key];
        }
    });

    // Gallery Section
    const galleryTitle = document.getElementById('gallery-title');
    const gallerySubtitle = document.getElementById('gallery-subtitle');
    
    if (galleryTitle) galleryTitle.textContent = content[lang].galleryTitle;
    if (gallerySubtitle) gallerySubtitle.textContent = content[lang].gallerySubtitle;

    // Location Section
    const mapTitle = document.getElementById('map-title');
    const mapSubtitle = document.getElementById('map-subtitle');
    const hotelLocation = document.getElementById('hotel-location');
    const contactAddress = document.getElementById('contact-address');
    const contactPhone = document.getElementById('contact-phone');
    const contactEmail = document.getElementById('contact-email');
    
    if (mapTitle) mapTitle.textContent = content[lang].mapTitle;
    if (mapSubtitle) mapSubtitle.textContent = content[lang].mapSubtitle;
    if (hotelLocation) hotelLocation.textContent = content[lang].hotelLocation;
    if (contactAddress) contactAddress.textContent = content[lang].contactAddress;
    if (contactPhone) contactPhone.textContent = content[lang].contactPhone;
    if (contactEmail) contactEmail.textContent = content[lang].contactEmail;

    // Footer
    const footerTagline = document.getElementById('footer-tagline');
    const footerServices = document.getElementById('footer-services');
    const footerContact = document.getElementById('footer-contact');
    const footerText = document.getElementById('footer-text');
    
    if (footerTagline) footerTagline.textContent = content[lang].footerTagline;
    if (footerServices) footerServices.textContent = content[lang].footerServices;
    if (footerContact) footerContact.textContent = content[lang].footerContact;
    if (footerText) footerText.textContent = content[lang].footerText;
    
    // Footer lists
    const footerServicesList = document.getElementById('footer-services-list');
    if (footerServicesList) {
        footerServicesList.innerHTML = '';
        content[lang].footerServicesList.forEach(service => {
            const li = document.createElement('li');
            li.textContent = service;
            footerServicesList.appendChild(li);
        });
    }
    
    const footerContactList = document.getElementById('footer-contact-list');
    if (footerContactList) {
        footerContactList.innerHTML = '';
        content[lang].footerContactList.forEach(contact => {
            const li = document.createElement('li');
            li.textContent = contact;
            footerContactList.appendChild(li);
        });
    }

    // Update language buttons
    document.getElementById('lang-en').classList.toggle('active', lang === 'en');
    document.getElementById('lang-tr').classList.toggle('active', lang === 'tr');
    document.getElementById('lang-de').classList.toggle('active', lang === 'de');
    document.getElementById('lang-ru').classList.toggle('active', lang === 'ru');
    
    // Update gallery captions
    updateCaptions(lang);
}

// Gallery and Lightbox functionality
const thumbs = Array.from(document.querySelectorAll('.thumb'));
let currentIndex = 0;

function updateCaptions(lang) {
    thumbs.forEach((fig, idx) => {
        const captionEl = fig.querySelector('.caption');
        const text = fig.dataset[`caption${lang === 'en' ? 'En' : 'Tr'}`];
        if (captionEl && text) {
            captionEl.textContent = text;
        }
        fig.dataset.index = idx;
    });
}

function openLightbox(index) {
    const fig = thumbs[index];
    const src = fig.dataset.src;
    const caption = fig.dataset[`caption${currentLang === 'en' ? 'En' : 'Tr'}`] || '';
    const lb = document.getElementById('lightbox');
    
    document.getElementById('lb-image').src = src;
    document.getElementById('lb-caption').textContent = caption;
    lb.classList.remove('hidden');
    lb.setAttribute('aria-hidden', 'false');
    currentIndex = index;
    
    // Prevent body scroll when lightbox is open
    document.body.style.overflow = 'hidden';
}

function closeLightbox() {
    const lb = document.getElementById('lightbox');
    document.getElementById('lb-image').src = '';
    lb.classList.add('hidden');
    lb.setAttribute('aria-hidden', 'true');
    
    // Restore body scroll
    document.body.style.overflow = '';
}

function showNext(dir = 1) {
    let next = (currentIndex + dir + thumbs.length) % thumbs.length;
    openLightbox(next);
}

// Event Listeners
document.addEventListener('DOMContentLoaded', function() {
    // Set default language
    setLanguage('en');
    
    // Language switching
    document.getElementById('lang-en').addEventListener('click', () => setLanguage('en'));
    document.getElementById('lang-tr').addEventListener('click', () => setLanguage('tr'));
    document.getElementById('lang-de').addEventListener('click', () => setLanguage('de'));
    document.getElementById('lang-ru').addEventListener('click', () => setLanguage('ru'));
    
    // Gallery thumbnails
    thumbs.forEach((fig, idx) => {
        fig.addEventListener('click', () => openLightbox(idx));
    });
    
    // Lightbox controls
    const lbClose = document.querySelector('.lb-close');
    const lbPrev = document.querySelector('.lb-prev');
    const lbNext = document.querySelector('.lb-next');
    const lightbox = document.getElementById('lightbox');
    
    if (lbClose) lbClose.addEventListener('click', closeLightbox);
    if (lbPrev) lbPrev.addEventListener('click', () => showNext(-1));
    if (lbNext) lbNext.addEventListener('click', () => showNext(1));
    
    // Click outside to close lightbox
    if (lightbox) {
        lightbox.addEventListener('click', (e) => {
            if (e.target.id === 'lightbox') closeLightbox();
        });
    }
    
    // Smooth scrolling for navigation links
    document.querySelectorAll('.nav-link').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            const targetSection = document.querySelector(targetId);
            
            if (targetSection) {
                const offsetTop = targetSection.offsetTop - 80; // Account for fixed navbar
                window.scrollTo({
                    top: offsetTop,
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Hero buttons functionality
    const bookNowBtn = document.getElementById('book-now-btn');
    const exploreBtn = document.getElementById('explore-btn');
    
    if (bookNowBtn) {
        bookNowBtn.addEventListener('click', () => {
            // Scroll to contact section or open booking modal
            const contactSection = document.getElementById('location');
            if (contactSection) {
                contactSection.scrollIntoView({ behavior: 'smooth' });
            }
        });
    }
    
    if (exploreBtn) {
        exploreBtn.addEventListener('click', () => {
            // Scroll to amenities section
            const amenitiesSection = document.getElementById('amenities');
            if (amenitiesSection) {
                amenitiesSection.scrollIntoView({ behavior: 'smooth' });
            }
        });
    }
});

// Keyboard navigation for lightbox
document.addEventListener('keydown', (e) => {
    const lb = document.getElementById('lightbox');
    if (lb && !lb.classList.contains('hidden')) {
        switch(e.key) {
            case 'ArrowRight':
                showNext(1);
                break;
            case 'ArrowLeft':
                showNext(-1);
                break;
            case 'Escape':
                closeLightbox();
                break;
        }
    }
});

